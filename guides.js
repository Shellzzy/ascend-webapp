// ===== Рендер карточек гайдов в окне BASE =====
function renderGuideList() {
  const list = document.getElementById('guide-list');
  if (!list) return;

  list.innerHTML = GUIDES.map(guide => `
    <button class="guide-card" data-guide="${guide.id}">
      <span class="guide-card-cover" style="background-image: url('${guide.cover}')"></span>
      <span class="guide-card-info">
        <span class="guide-card-title">${guide.title}</span>
        <span class="guide-card-short">${guide.short}</span>
      </span>
    </button>
  `).join('');

  list.querySelectorAll('.guide-card').forEach(card => {
    card.addEventListener('click', () => openGuide(card.dataset.guide));
  });
}

// ===== Полноэкранная страница-читалка гайда =====
function openGuide(id) {
  const guide = GUIDES.find(g => g.id === id);
  if (!guide) return;

  const reader = document.getElementById('guide-reader');

  reader.innerHTML = `
    <div class="window-header reader-header">
      <button class="reader-back" id="reader-back" aria-label="Назад к BASE">&lt; BASE</button>
      <span class="window-title">// ${guide.title.toUpperCase().replace(/\s+/g, '-')}.EXE</span>
      <span class="window-controls"><i>_</i><i>□</i><i>✖</i></span>
    </div>
    <div class="reader-body">
      ${guide.sections.map(section => `
        <section class="reader-section">
          <h3 class="reader-heading">${section.heading}</h3>
          ${section.image ? `<div class="reader-image-frame"><img src="${section.image}" alt="" loading="lazy" /></div>` : ''}
          ${section.text.map(p => `<p class="reader-text">${p}</p>`).join('')}
        </section>
      `).join('')}
    </div>
  `;

  document.getElementById('reader-back').addEventListener('click', closeGuide);

  reader.classList.add('active');
  reader.scrollTop = 0;
  document.body.style.overflow = 'hidden';
}

function closeGuide() {
  const reader = document.getElementById('guide-reader');
  reader.classList.remove('active');
  document.body.style.overflow = 'auto';
}

document.addEventListener('DOMContentLoaded', renderGuideList);
